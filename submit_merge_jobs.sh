#!/bin/bash
# Submits one independent SLURM job per merged sample.
# Run from the project directory: bash submit_merge_jobs.sh

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
JOB_SCRIPT="${SCRIPT_DIR}/batch_merge_processing.sh"

if [ ! -f "${JOB_SCRIPT}" ]; then
    echo "Error: batch_merge_processing.sh not found at ${SCRIPT_DIR}"
    exit 1
fi

mkdir -p "${SCRIPT_DIR}/logs"

submit() {
    local name="$1"
    local srrs="$2"
    local job_id
    job_id=$(sbatch \
        --export=ALL,SAMPLE_NAME="${name}",SRRS="${srrs}" \
        --job-name="merge_${name}" \
        "${JOB_SCRIPT}" | awk '{print $NF}')
    echo "Submitted job ${job_id}: ${name}"
}

